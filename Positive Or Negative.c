//To find the number is positive or negative
#include <stdio.h>
void main()
{
    int num;
    printf("\nEnter a Number: ");
    scanf("%d",&num);
    if (num >0)
    {
        printf("\nThe number is positive",num);
    }

    else (num <0);
    {
        printf("\nThe number is negative",num);
    }
}